package br.com.appizza.formas;

/**
 *
 * @author Pichau
 */
public class Tipo {
    private int idTipo;
    private String tipo;
    private float valorCmQuadrado;

    public int getIdTipo() {
        return idTipo;
    }

    public void setIdTipo(int idTipo) {
        this.idTipo = idTipo;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public float getValorCmQuadrado() {
        return valorCmQuadrado;
    }

    public void setValorCmQuadrado(float valorCmQuadrado) {
        this.valorCmQuadrado = valorCmQuadrado;
    }
    
}
